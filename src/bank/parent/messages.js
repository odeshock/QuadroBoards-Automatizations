const sendQueue = [];
let sending = false;

async function processQueue() {
  if (sending) return;
  sending = true;
  console.log(`üü¶ [STEP] send queue started (items: ${sendQueue.length})`);
  try {
    while (sendQueue.length) {
      const task = sendQueue.shift();
      try {
        await task();
      } catch (e) {
        console.warn("‚ùå [ERROR] send task failed:", e?.message || e);
      }
      await humanPause(SEND_BASE_GAP_MS, SEND_JITTER_MS, "gap between sends");
    }
  } finally {
    sending = false;
    console.log("‚úÖ [OK]   send queue drained");
  }
}

// –¥–æ–±–∞–≤–ª—è–µ—Ç –∑–∞–¥–∞—á—É –æ—Ç–ø—Ä–∞–≤–∫–∏ –ø—Ä–æ–∏–∑–≤–æ–ª—å–Ω–æ–π —Ä–∞–±–æ—Ç—ã, –∑–∞–≤–∏—Å—è—â–µ–π –æ—Ç iframe
function queueJob(iframeReadyP, jobFactory /* (iframeWindow) => Promise<void> */) {
  iframeReadyP.then((iframeWindow) => {
    sendQueue.push(async () => {
      const startedAt = Date.now();
      console.log("üü™ [QUEUE] job started");
      await jobFactory(iframeWindow);
      console.log(`üü© [SENT]  job done in ${Date.now() - startedAt}ms`);
    });
    console.log(`üü™ [QUEUE] job enqueued (size: ${sendQueue.length})`);
    processQueue();
  }).catch(err => console.warn("queueJob skipped:", err?.message || err));
}

